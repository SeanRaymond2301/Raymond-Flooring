<h2>Engineered Hardwood</h2>

<p>Pro's</p>
<ul>
    <li>Makes a slight creek sound when stepping on it.</li>
    <li>Very similar looking to real wood.</li>
    <li>Soft like vinyl plank</li>
</ul>

<br>

<p>Con's</p>
<ul>
    <li>Susceptible to water damage</li>
    <li>If glued down, it may be difficult to replace board.</li>
    <li>Most expensive, and hardest to install (only if glued down, can be glued or free-floated).</li>
    <li>Requires molding around outside doors, flooring changes, etc...</li>
    <li>Engineered Hardwood expands and contracts in different temperatures and humidities.</li>
</ul>
